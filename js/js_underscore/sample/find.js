// ============================================================
//  find
//
//      遍歷 list， 返回第一個通過 iterator 真值檢測的元素值
//      如果找到匹配的元素立即返回，不會遍歷整個 list
//
// ============================================================

var func = function(num) 
{ 
    return num % 2 == 0; 
};

var even = _.find([1, 2, 3, 3, 2, 1], func );

log(even);
